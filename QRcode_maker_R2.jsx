//mask
function masking(mdlsNum, mask) {
	var i, j;

	for (i=0;i<mdlsNum;i++) {
		for (j=0;j<mdlsNum;j++) {
			bitArry[i][j] = symArry[i][j] & 0x1;
			if (!(symArry[i][j] & 0xc0)) {
				switch (mask) {
					case 0x0:  // (i+j)mod 2=0
						if ((i+j) % 2 == 0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x1:  // i mod 2=0
						if ((i % 2)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x2:  // j mod 3=0
						if ((j % 3)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x3:  // (i+j)mod 3=0
						if (((i+j) % 3)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x4:  // ((i div 2)+(j div 3))mod 2=0
						if (((Math.floor(i/2)+Math.floor(j/3)) % 2)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x5:  // (i j)mod 2+(i j)mod 3=0
						if ((i*j % 2+i*j % 3)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x6:  // ((i j)mod 2+(i j)mod 3)mod 2=0
						if (((i*j % 2+i*j % 3) % 2)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
					case 0x7:  // ((i j)mod 3+(i+j)mod 2)mod 2=0
						if (((i*j % 3+(i+j) % 2) % 2)==0) {
							bitArry[i][j] ^= 0x1;
							}
							break;
						}
					}
			 }
		}
	code1 = frmInf[level][mask];
	b1 = 0x4000;
	for (i=0;i<7;i++) {
		bitArry[8][(i<6) ? i : i+1] = bitArry[mdlsNum-1-i][8] = (code1 & b1) ? 1 : 0;
		b1 >>= 1;
		}
	for (i=7; i>=0;i--) {
		bitArry[(i<6) ? i : i + 1][8] = bitArry[8][mdlsNum-1-i] = (code1 & b1) ? 1 : 0;
		b1 >>= 1;
		}
	if (version >= 7) {
		code1 = vrInf[version-7];
		b1 = 0x20000;
		for (i=0;i<18;i++) {
			y = mdlsNum - 9 - i % 3;
			x = 5-Math.floor(i / 3);
			bitArry[y][x] = bitArry[x][y] = (code1 & b1) ? 1 : 0;
			b1 >>= 1;
			}
		}
	}



